<h3>What is CyclonePHP?</h3>

CyclonePHP is a featureful, enterprise-ready application development
framework for PHP.

<h3>Main features</h3>
<ul>
	<li>fast bootstrap mechanism</li>
	<li>heavyweight ORM library</li>
	<li>RAD features</li>
	<li>originally built on Kohana</li>
	<li>CSSMin, JSMin integration</li>
	<li>Easy integration with other libraries</li>
	<li>Loosely coupled, CyclonePHP libraries can be used as standalone libraries</li>
</ul>

<h3>When to use CyclonePHP?</h3>
<p>CyclonePHP is designed for building big applications. For example if you have to deal with a
complex model layer, or you have to create tons of forms quickly then you will be
productive with CyclonePHP. If you have to build...
<ul>
	<li>a complex intranet business application, then CyclonePHP is an excellent choice for you.</li>
	<li>a new web-based service with special needs and you require high scalability, then you are good with CyclonePHP.</li>
	<li>a middle-scale application with a fairly big amount of server-side code, then you will find it much simpler with CyclonePHP then with an other framework.</li>
	<li>a few thousand-line application in a few weeks, then you can taste the power of CyclonePHP, but maybe putting the environment together and learning the framework will need relatively a lot of time.</li>
	<li>a very simple informational site that is mainly about design and client-side code with minimal PHP, then CyclonePHP is not the best choice for you.</li>
</ul>
</p>

<h3>Is CyclonePHP a framework?</h3>
<p>CyclonePHP provides some general functionality that every framework should provide, like autoloader, general file system layout, request dispatcher etc. But it's main features like easy form management and data modeling can be used as stand-alone libraries too. It's more precise to say that CyclonePHP is a set of libraries designed to well operate together.</p>


<h3>What are the key components of CyclonePHP?</h3>
<ul>
	<li>lightweight web layer</li>
	<li>CyForm: form management library</li>
	<li>JORK: heavyweight but fast ORM library</li>
</ul>
