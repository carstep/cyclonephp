<h3>What is CyclonePHP?</h3>

CyclonePHP is a featureful, enterprise-ready application development
framework for PHP.

<h3>Main features</h3>
<ul>
	<li>fast bootstrap mechanism</li>
	<li>heavyweight ORM library</li>
	<li>RAD features</li>
	<li>originally built on Kohana</li>
	<li>CSSMin, JSMin integration</li>
	<li>Easy integration with other libraries</li>
	<li>Loosely coupled, CyclonePHP libraries can be used as standalone libraries</li>
</ul>

